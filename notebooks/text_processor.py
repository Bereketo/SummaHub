class TextProcessor:
    @staticmethod
    def summarize_text(dialogue: str) -> str:
        """
        Generates a summary for the given dialogue.
        """
        # Placeholder for summarization logic
        return f"Summary of the dialogue: {dialogue[:100]}..."

    @staticmethod
    def generate_question(dialogue: str) -> str:
        """
        Generates a question based on the given dialogue.
        """
        # Placeholder for question generation logic
        return f"What is the main topic of: {dialogue[:100]}?"
